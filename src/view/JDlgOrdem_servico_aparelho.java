    private void jBtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPesquisarActionPerformed
        // TODO add your handling code here:
         String id = JOptionPane.showInputDialog(null, "Entre com o código");
        int codigo = Integer.parseInt(id);
        Ordem_servico_aparelhoDao ordem_servico_aparelhoDao = new Ordem_servico_aparelhoDao();
        Ordem_servico_AparelhoBean ordem_servico_aparelho = (Ordem_servico_AparelhoBean) ordem_servico_aparelhoDao.list(codigo);
        if (ordem_servico_aparelho ==  null){
            JOptionPane.showMessageDialog(null, "Codigo Não encontrado");
        }
        else{
            jTxtCod.setText(id);
       jFmtOs.setText(String.valueOf(ordem_servico_aparelho.getMsc_fkOs()));
       jFmtAparelho.setText(String.valueOf(ordem_servico_aparelho.getMsc_fkAparelho()));
        jTxtOBS.setText(ordem_servico_aparelho.getMsc_observacoes());   
       jTxtValorunit.setText(String.valueOf(ordem_servico_aparelho.getMsc_valor_unitario()));
       jFmtQuant.setText(String.valueOf(ordem_servico_aparelho.getMsc_quantidade()));
       

      
        }
    }//GEN-LAST:event_jBtnPesquisarActionPerformed

