    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        //desabilitar();
         habilitar(false);
         Ordem_servico_AparelhoBean ordem_servico_Aparelho = new Ordem_servico_AparelhoBean();
    int cod = Integer.parseInt(jTxtCod.getText());
    ordem_servico_Aparelho.setMsc_idOsAparelho(cod);
    ordem_servico_Aparelho.setMsc_fkOs(Integer.parseInt(jFmtOs.getText()));
ordem_servico_Aparelho.setMsc_fkAparelho(Integer.parseInt(jFmtAparelho.getText()));
ordem_servico_Aparelho.setMsc_observacoes(jTxtOBS.getText());
ordem_servico_Aparelho.setMsc_valor_unitario(Double.valueOf(jTxtValorunit.getText()));
ordem_servico_Aparelho.setMsc_quantidade(Integer.parseInt(jFmtQuant.getText()));

     Ordem_servico_aparelhoDao dao = new Ordem_servico_aparelhoDao();
    
     Ordem_servico_AparelhoBean existente = (Ordem_servico_AparelhoBean) dao.list(cod);
    if (existente == null) {
        dao.insert(ordem_servico_Aparelho);
        JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");
    } else {
        dao.update(ordem_servico_Aparelho);
        JOptionPane.showMessageDialog(this, "Usuário alterado com sucesso!");
    }
    
 limpar();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

